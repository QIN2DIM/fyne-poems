type Selector struct {
	corpus []string
	cursor int
}

func NewSelector() *Selector {
	s := &Selector{
		corpus: corpus,
		cursor: 0,
	}
	s.init()
	return s
}

func (s *Selector) init() {
	rand.Seed(time.Now().UnixNano())
	rand.Shuffle(len(s.corpus), func(i, j int) {
		s.corpus[i], s.corpus[j] = s.corpus[j], s.corpus[i]
	})
	s.cursor = (len(s.corpus) - 1) / 2
}

func (s *Selector) GetCurrentCorpus() (poem string) {
	defer func() {
		if r := recover(); r != nil {
			poem = s.GetNextCorpus()
		}
	}()
	return s.corpus[s.cursor]
}

func (s *Selector) GetNextCorpus() string {
	if s.cursor <= len(s.corpus)-2 {
		s.cursor += 1
		return s.corpus[s.cursor]
	}
	s.init()
	return s.GetNextCorpus()
}

func (s *Selector) GetPreviousCorpus() string {
	if s.cursor >= 1 {
		s.cursor -= 1
		return s.corpus[s.cursor]
	}
	s.init()
	return s.GetPreviousCorpus()
}
